    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked

        if (jTable1.getSelectedRow() != -1) {

            jTextField2.setText(String.valueOf(jTable1.getValueAt(jTable1.getSelectedRow(), 1)));
            jTextArea1.setText(String.valueOf(jTable1.getValueAt(jTable1.getSelectedRow(), 2)));

            try {

                ResultSet resultSet = MySQL.executeSearch("SELECT * FROM `class` "
                        + "WHERE `subject_sub_no`='" + String.valueOf(jTable1.getValueAt(jTable1.getSelectedRow(), 0)) + "'");

                if (resultSet.next()) {
                    jTextField2.setEditable(false);
                    jTextArea1.setEditable(false);
                    jButton4.setEnabled(false);
                } else {
                    jTextField2.setEditable(true);
                    jTextArea1.setEditable(true);
                    jButton4.setEnabled(true);
                }

                jButton3.setEnabled(false);

            } catch (Exception e) {
                e.printStackTrace();
            }

        }

    }//GEN-LAST:event_jTable1MouseClicked

    private void jButton7ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton7ActionPerformed

        if (jTable1.getRowCount() > 0) {

            try {

                InputStream s = this.getClass().getResourceAsStream("/com/sahan/adyapana/reports/ai_subjects.jasper");

                HashMap<String, Object> param = new HashMap<>();

                param.put("Parameter1", new SimpleDateFormat("yyyy-MM-dd").format(new Date()));
                param.put("Parameter2", new SimpleDateFormat("hh:mm:ss a").format(new Date()));

                JRTableModelDataSource dataSources = new JRTableModelDataSource(jTable1.getModel());

                JasperPrint report = JasperFillManager.fillReport(s, param, dataSources);

                JasperViewer.viewReport(report, false);

            } catch (Exception e) {
                e.printStackTrace();
            }

        } else {

            JOptionPane.showMessageDialog(this, "No data in the table.",
                    "Warning", JOptionPane.WARNING_MESSAGE);

            jTable1.grabFocus();

        }

    }//GEN-LAST:event_jButton7ActionPerformed

