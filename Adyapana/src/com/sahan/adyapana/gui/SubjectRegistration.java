    private void LoadSubjects() {

        if (jButton3.isEnabled()) {

            try {

                String query = "SELECT * FROM `subject` ORDER BY ";

                String sort = String.valueOf(jComboBox3.getSelectedItem());

                if (sort == "Subject No ASC") {
                    query += "`sub_no` ASC";
                } else if (sort == "Subject No DESC") {
                    query += "`sub_no` DESC";
                } else if (sort == "Subject ASC") {
                    query += "`name` ASC";
                } else if (sort == "Subject DESC") {
                    query += "`name` DESC";
                }

                ResultSet resultSet = MySQL.executeSearch(query);

                DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
                model.setRowCount(0);

                while (resultSet.next()) {

                    Vector<String> vector = new Vector<>();
                    vector.add(resultSet.getString("sub_no"));
                    vector.add(resultSet.getString("name"));
                    vector.add(resultSet.getString("description"));

                    model.addRow(vector);

                }

                jTable1.setModel(model);

            } catch (Exception e) {
                e.printStackTrace();
            }

        }

    }

