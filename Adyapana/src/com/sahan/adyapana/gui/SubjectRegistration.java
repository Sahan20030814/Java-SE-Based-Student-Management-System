    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed

        if (jTextField2.getText().isEmpty()) {

            JOptionPane.showMessageDialog(this, "Please Enter the subject name first.",
                    "Warning", JOptionPane.WARNING_MESSAGE);

            jTextField2.grabFocus();

        } else if (jTextField2.getText().length() > 30) {

            JOptionPane.showMessageDialog(this, "Subject Name must contain less than 30 characters.",
                    "Warning", JOptionPane.WARNING_MESSAGE);

            jTextField2.grabFocus();

        } else if (jTextArea1.getText().isEmpty()) {

            JOptionPane.showMessageDialog(this, "Please Enter a description about the subject.",
                    "Warning", JOptionPane.WARNING_MESSAGE);

            jTextArea1.grabFocus();

        } else if (jTextArea1.getText().length() > 100) {

            JOptionPane.showMessageDialog(this, "Mini Description must contain less than 100 characters.",
                    "Warning", JOptionPane.WARNING_MESSAGE);

            jTextArea1.grabFocus();

        } else {

            String name = jTextField2.getText();
            String description = jTextArea1.getText();

            try {

                ResultSet resultSet = MySQL.executeSearch("SELECT * FROM `subject` WHERE `name`='" + name + "'");

                if (resultSet.next()) {

                    JOptionPane.showMessageDialog(this, "There is already a registered subject with the same name.",
                            "Error", JOptionPane.ERROR_MESSAGE);

                    jTable1.grabFocus();

                } else {

                    MySQL.executeIUD("INSERT INTO `subject` (`name`,`description`) VALUES ('" + name + "','" + description + "')");

                    Reset();
                    LoadSubjects();

                    Notifications.getInstance().show(Notifications.Type.SUCCESS, Notifications.Location.TOP_CENTER, 2500L, "New Subject registered Successfully!");

                }

            } catch (Exception e) {
                e.printStackTrace();
            }

        }

    }//GEN-LAST:event_jButton3ActionPerformed

