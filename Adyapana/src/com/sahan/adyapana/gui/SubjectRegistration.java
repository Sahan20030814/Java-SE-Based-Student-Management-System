    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed

        if (jTable1.getSelectedRow() == -1) {

            JOptionPane.showMessageDialog(this, "Please Select a subject to update.",
                    "Error", JOptionPane.ERROR_MESSAGE);

            jTable1.grabFocus();

        } else {

            if (jTextField2.getText().isEmpty()) {

                JOptionPane.showMessageDialog(this, "Please Enter the subject name first.",
                        "Warning", JOptionPane.WARNING_MESSAGE);

                jTextField2.grabFocus();

            } else if (jTextField2.getText().length() > 30) {

                JOptionPane.showMessageDialog(this, "Subject Name must contain less than 30 characters.",
                        "Warning", JOptionPane.WARNING_MESSAGE);

                jTextField2.grabFocus();

            } else if (jTextArea1.getText().isEmpty()) {

                JOptionPane.showMessageDialog(this, "Please Enter a description about the subject.",
                        "Warning", JOptionPane.WARNING_MESSAGE);

                jTextArea1.grabFocus();

            } else if (jTextArea1.getText().length() > 100) {

                JOptionPane.showMessageDialog(this, "Mini Description must contain less than 100 characters.",
                        "Warning", JOptionPane.WARNING_MESSAGE);

                jTextArea1.grabFocus();

            } else {

                String sub_no = String.valueOf(jTable1.getValueAt(jTable1.getSelectedRow(), 0));
                String name = jTextField2.getText();
                String description = jTextArea1.getText();

                String selected_name = String.valueOf(jTable1.getValueAt(jTable1.getSelectedRow(), 1));
                String selected_description = String.valueOf(jTable1.getValueAt(jTable1.getSelectedRow(), 2));

                if (name.equals(selected_name) && description.equals(selected_description)) {

                    JOptionPane.showMessageDialog(this, "Same data in database",
                            "Warning", JOptionPane.WARNING_MESSAGE);

                    jTable1.grabFocus();

                } else {

                    try {

                        ResultSet resultSet = MySQL.executeSearch("SELECT * FROM `subject` WHERE `name`='" + name + "' AND NOT `sub_no`='" + sub_no + "'");

                        if (resultSet.next()) {

                            JOptionPane.showMessageDialog(this, "There is already a registered subject with the same name.",
                                    "Error", JOptionPane.ERROR_MESSAGE);

                            jTable1.grabFocus();

                        } else {

                            int option = JOptionPane.showConfirmDialog(this, "Are you sure?", "Confirm",
                                    JOptionPane.YES_NO_OPTION, JOptionPane.WARNING_MESSAGE);

                            if (option == JOptionPane.YES_OPTION) {

                                MySQL.executeIUD("UPDATE `subject` SET `name`='" + name + "',`description`='" + description + "' WHERE `sub_no`='" + sub_no + "'");

                                Reset();
                                LoadSubjects();

                                Notifications.getInstance().show(Notifications.Type.SUCCESS, Notifications.Location.TOP_CENTER,
                                        2500L, "Selected Subject(" + sub_no + ") Details updated Successfully!");

                            }

                        }

                    } catch (Exception e) {
                        e.printStackTrace();
                    }

                }

            }

        }

    }//GEN-LAST:event_jButton4ActionPerformed


